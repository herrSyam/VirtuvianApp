package com.example.virtuvianapplication.model;

public class PersonObatModel {
    private String userId;
    private String obatId;
    private String userName;
    private String obatName;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getObatId() {
        return obatId;
    }

    public void setObatId(String obatId) {
        this.obatId = obatId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getObatName() {
        return obatName;
    }

    public void setObatName(String obatName) {
        this.obatName = obatName;
    }
}
